// While and do while loop:-

let i = 1;

while( i<=10 ){
    console.log(i);
    i++;
}

let arr = [ 'flash', 'superman', 'spiderman'];
let index=0;
while( index<arr.length ){
    console.log(arr[index]);
    index++;
}

// do while loop:- it will execute atleast once, first print then condition check!

let j = 1;

do{
    console.log(j);
    j++;
}while( j<=10);